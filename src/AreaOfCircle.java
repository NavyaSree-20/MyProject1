public class AreaOfCircle{
    public static void main(String[] args) {
        double areaOfCircle;
        double pi=3.14159;
        int radius=8;
        System.out.println("Pi is :"+pi+" radius is:"+radius);
        areaOfCircle=pi*radius*radius;
        System.out.println("Area of circle :"+areaOfCircle);
    }
}